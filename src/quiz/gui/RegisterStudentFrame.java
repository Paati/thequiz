    public int checkInput()
{
          
if(txtUserId.getText().isEmpty()||txtPass.getPassword().toString().isEmpty()||txtRePass.getPassword().toString().isEmpty())
return 0;
else if(txtUserId.getText().length()<4||String.valueOf(txtPass.getPassword()).length()<4)
        return -1;
else if(String.valueOf(txtPass.getPassword()).equals(String.valueOf(txtRePass.getPassword()))==false) 
       return -2;
else return 1;

}
