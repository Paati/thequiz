    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
       if(checkInput()==1)
       {          
             u.setId(txtUserId.getText());
             u.setPwd(String.valueOf(txtPass.getPassword()));
             u.setType("student");
                 try {
               boolean b= userDAO.registerStudent(u);
               if(b==true){
                   JOptionPane.showMessageDialog(null,"Registered successfully","Done !!",JOptionPane.INFORMATION_MESSAGE);
                   clearAll();
                   AdminScreen admin=new AdminScreen();
                   admin.setVisible(true);
                   this.dispose();
                   
               }
            } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null,"Enter another id"+ex,"Eror",JOptionPane.ERROR_MESSAGE);
            }}
        
       else if(checkInput()==0)
       {JOptionPane.showMessageDialog(null,"Fill all entries..","Eror",JOptionPane.ERROR_MESSAGE);
       return;
       }
       else if(checkInput()==-1)
                  {         JOptionPane.showMessageDialog(null,"Password should be of more than 4 letters","Error",JOptionPane.ERROR_MESSAGE);
                  return;
                  }
                  else
       {  JOptionPane.showMessageDialog(null,"Entered password didnot matched","Error",JOptionPane.ERROR_MESSAGE);
           return;}
    }//GEN-LAST:event_btnRegisterActionPerformed

